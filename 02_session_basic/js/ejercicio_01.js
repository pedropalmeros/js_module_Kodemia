let n1 = Number(prompt("Introduce el primer número"));
let n2 = Number(prompt("Introduce el segundo número"));

console.log(`La suma de ${n1} + ${n2} es: ${n1+n2}`)
console.log(`La resta de ${n1} - ${n2} es: ${n1-n2}`)
console.log(`La producto de ${n1} * ${n2} es: ${n1*n2}`)
console.log(`La division de ${n1} / ${n2} es: ${n1/n2}`)
console.log(`La módulo de ${n1} % ${n2} es: ${n1%n2}`)




